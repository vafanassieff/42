#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	unsigned char	*cur;

	if (len == 0)
		return (b);
	cur = (unsigned char *)b;
	while (len--)
	{
		*cur = 0;
		if (len)
			cur++;
	}
	return (b);
}
