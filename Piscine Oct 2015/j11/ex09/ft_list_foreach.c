#include "ft_list.h"
#include <stdlib.h>

void	ft_list_foreach(t_list *begin_list, void (*f)(void *))
{
	if (begin_list == NULL)
		return ;
	f(begin_list->data);
	if (begin_list->next)
		ft_list_foreach(begin_list->next, f);
}
