int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ct;

	ct = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			ct++;
		i++;
	}
	return (ct);
}
